package exemplos;

public class Exemplo02 {
    public static void main(String[] args) {
        int numero; // declaração ou criação da variável.

        numero = 10; // atribuição - guardar valor da variável.
        System.out.println(numero);

        numero = 5;
        System.out.println(numero);

        numero = 7 * 2;
        System.out.println(numero);

        numero = numero + 1;
        System.out.println(numero);
    }
}
